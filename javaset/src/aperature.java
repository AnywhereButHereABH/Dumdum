

public class aperature { 
    public static void main(String[] args) { 
        /* Assignment Operators */

        int x = 100;
        int y = 20;

        x += y;
        System.out.println(x);
        x -= y;
        System.out.println(x);
        x *= y;
        System.out.println(x);
        x /= y;
        System.out.println(x);
        x %= y;
        System.out.println(x);

        x++;
        System.out.println(x);

        x--;
        System.out.println(x);
    }
}
